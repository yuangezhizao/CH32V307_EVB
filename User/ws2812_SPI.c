#include <ws2812_SPI.h>
#include "debug.h"

uint8_t pixelBuffer[PIXEL_NUM][24];

void ws281x_init(void)
{
  GPIO_InitTypeDef GPIO_InitStructure;
  SPI_InitTypeDef SPI_InitStructure;
  DMA_InitTypeDef DMA_InitStructure;

  RCC_APB2PeriphClockCmd(RCC_APB2Periph_GPIOB, ENABLE); // PORTB时钟使能
  RCC_APB1PeriphClockCmd(RCC_APB1Periph_SPI2, ENABLE);  // SPI2时钟使能
  RCC_AHBPeriphClockCmd(RCC_AHBPeriph_DMA1, ENABLE);    //使能DMA传输

  /* PA7  SPI1_MOSI */
  GPIO_InitStructure.GPIO_Pin = GPIO_Pin_15;
  GPIO_InitStructure.GPIO_Mode = GPIO_Mode_AF_PP; // Pb15复用推挽输出 SPI
  GPIO_InitStructure.GPIO_Speed = GPIO_Speed_50MHz;
  GPIO_Init(GPIOB, &GPIO_InitStructure); //初始化GPIOB

  SPI_InitStructure.SPI_Direction = SPI_Direction_1Line_Tx; //设置SPI单向或者双向的数据模式:SPI设置为双线双向全双工
  SPI_InitStructure.SPI_Mode = SPI_Mode_Master;             //设置SPI工作模式:设置为主SPI
  SPI_InitStructure.SPI_DataSize = SPI_DataSize_8b;         //设置SPI的数据大小:SPI发送接收8位帧结构
  // SPI_InitStructure.SPI_DataSize = SPI_DataSize_16b;                  //设置SPI的数据大小:SPI发送接收8位帧结构
  SPI_InitStructure.SPI_CPOL = SPI_CPOL_Low;   //串行同步时钟的空闲状态为低电平
  SPI_InitStructure.SPI_CPHA = SPI_CPHA_2Edge; //串行同步时钟的第2个跳变沿（上升或下降）数据被采样
                                               //  SPI_InitStructure.SPI_CPHA = SPI_CPHA_1Edge; //串行同步时钟的第2个跳变沿（上升或下降）数据被采样
  SPI_InitStructure.SPI_NSS = SPI_NSS_Soft;    // NSS信号由硬件（NSS管脚）还是软件（使用SSI位）管理:内部NSS信号有SSI位控制
  //  SPI_InitStructure.SPI_BaudRatePrescaler = SPI_BaudRatePrescaler_8; //定义波特率预分频的值:波特率预分频值为16
  SPI_InitStructure.SPI_BaudRatePrescaler = SPI_BaudRatePrescaler_16; //定义波特率预分频的值:波特率预分频值为16
  SPI_InitStructure.SPI_FirstBit = SPI_FirstBit_MSB;                  //指定数据传输从MSB位还是LSB位开始:数据传输从MSB位开始
  SPI_InitStructure.SPI_CRCPolynomial = 7;                            // CRC值计算的多项式
  SPI_Init(SPI2, &SPI_InitStructure);                                 //根据SPI_InitStruct中指定的参数初始化外设SPIx寄存器

  SPI_I2S_DMACmd(SPI2, SPI_I2S_DMAReq_Tx, ENABLE);

  SPI_Cmd(SPI2, ENABLE); //使能SPI2外设

  DMA_DeInit(DMA1_Channel5);                                              //将DMA的通道1寄存器重设为缺省值
  DMA_InitStructure.DMA_BufferSize = PIXEL_NUM * 24;                      // cndtr;  //DMA通道的DMA缓存的大小
  DMA_InitStructure.DMA_PeripheralBaseAddr = (uint32_t) & (SPI2->DATAR);  // cpar;  //DMA外设ADC基地址
  DMA_InitStructure.DMA_MemoryBaseAddr = (uint32_t)pixelBuffer;           // cmar;  //DMA内存基地址
  DMA_InitStructure.DMA_DIR = DMA_DIR_PeripheralDST;                      //数据传输方向，从内存读取发送到外设
                                                                          //  DMA_InitStructure.DMA_Priority = DMA_Priority_Medium;                  // DMA通道 x拥有中优先级
  DMA_InitStructure.DMA_Priority = DMA_Priority_Low;                      // DMA通道 x拥有中优先级
  DMA_InitStructure.DMA_PeripheralInc = DMA_PeripheralInc_Disable;        //外设地址寄存器不变
  DMA_InitStructure.DMA_MemoryInc = DMA_MemoryInc_Enable;                 //内存地址寄存器递增
  DMA_InitStructure.DMA_PeripheralDataSize = DMA_PeripheralDataSize_Byte; //数据宽度为8位
  DMA_InitStructure.DMA_MemoryDataSize = DMA_MemoryDataSize_Byte;         //数据宽度为8位
  // DMA_InitStructure.DMA_PeripheralDataSize = DMA_PeripheralDataSize_HalfWord;
  // DMA_InitStructure.DMA_MemoryDataSize = DMA_MemoryDataSize_HalfWord;
  DMA_InitStructure.DMA_Mode = DMA_Mode_Normal; //工作在正常缓存模式
  DMA_InitStructure.DMA_M2M = DMA_M2M_Disable;  // DMA通道x没有设置为内存到内存传输
  DMA_Init(DMA1_Channel5, &DMA_InitStructure);  //根据DMA_InitStruct中指定的参数初始化DMA的通道USART1_Tx_DMA_Channel所标识的寄存器

  ws281x_closeAll(); //关闭全部的灯
  Delay_Ms(100);     //关闭全部的灯需要一定的时间
}

void ws281x_closeAll(void)
{
  uint16_t i;
  uint8_t j;

  for (i = 0; i < PIXEL_NUM; ++i)
  {
    for (j = 0; j < 24; ++j)
    {
      pixelBuffer[i][j] = WS_LOW;
    }
  }
  ws281x_show();
}

uint32_t ws281x_color(uint8_t red, uint8_t green, uint8_t blue)
{
  return green << 16 | red << 8 | blue;
}

void ws281x_setPixelColor(uint16_t n, uint32_t GRBcolor)
{
  uint8_t i;
  if (n < PIXEL_NUM)
  {
    for (i = 0; i < 24; ++i)
    {
      pixelBuffer[n][i] = (((GRBcolor << i) & 0X800000) ? WS_HIGH : WS_LOW);
    }
  }
}

void ws281x_setPixelRGB(uint16_t n, uint8_t red, uint8_t green, uint8_t blue)
{
  uint8_t i;

  if (n < PIXEL_NUM)
  {
    for (i = 0; i < 24; ++i)
    {
      pixelBuffer[n][i] = (((ws281x_color(red, green, blue) << i) & 0X800000) ? WS_HIGH : WS_LOW);
    }
  }
}

void ws281x_show(void)
{
  DMA_Cmd(DMA1_Channel5, DISABLE); //关闭 TX DMA1 所指示的通道
  DMA_ClearFlag(DMA1_FLAG_TC3);
  DMA_SetCurrDataCounter(DMA1_Channel5, 24 * PIXEL_NUM); // DMA通道的DMA缓存的大小
  DMA_Cmd(DMA1_Channel5, ENABLE);                        //使能 TX DMA1 所指示的通道
}

// Input a value 0 to 255 to get a color value.
// The colours are a transition r - g - b - back to r.
uint32_t ws281x_wheel(uint8_t wheelPos)
{
  wheelPos = 255 - wheelPos;
  if (wheelPos < 85)
  {
    return ws281x_color(255 - wheelPos * 3, 0, wheelPos * 3);
  }
  if (wheelPos < 170)
  {
    wheelPos -= 85;
    return ws281x_color(0, wheelPos * 3, 255 - wheelPos * 3);
  }
  wheelPos -= 170;
  return ws281x_color(wheelPos * 3, 255 - wheelPos * 3, 0);
}

// Fill the dots one after the other with a color
void ws281x_colorWipe(uint32_t c, uint8_t wait)
{
  for (uint16_t i = 0; i < PIXEL_NUM; i++)
  {
    ws281x_setPixelColor(i, c);
    ws281x_show();
    Delay_Ms(wait);
  }
}

void ws281x_rainbow(uint8_t wait)
{
  uint16_t i, j;

  for (j = 0; j < 256; j++)
  {
    for (i = 0; i < PIXEL_NUM; i++)
    {
      ws281x_setPixelColor(i, ws281x_wheel((i + j) & 255));
    }
    ws281x_show();
    Delay_Ms(wait);
  }
}

// Slightly different, this makes the rainbow equally distributed throughout
void ws281x_rainbowCycle(uint8_t wait)
{
  uint16_t i, j;

  for (j = 0; j < 256 * 5; j++)
  { // 5 cycles of all colors on wheel
    for (i = 0; i < PIXEL_NUM; i++)
    {
      ws281x_setPixelColor(i, ws281x_wheel(((i * 256 / PIXEL_NUM) + j) & 255));
    }
    ws281x_show();
    Delay_Ms(wait);
  }
}

// Theatre-style crawling lights.
void ws281x_theaterChase(uint32_t c, uint8_t wait)
{
  for (int j = 0; j < 10; j++)
  { // do 10 cycles of chasing
    for (int q = 0; q < 3; q++)
    {
      for (uint16_t i = 0; i < PIXEL_NUM; i = i + 3)
      {
        ws281x_setPixelColor(i + q, c); // turn every third pixel on
      }
      ws281x_show();

      Delay_Ms(wait);

      for (uint16_t i = 0; i < PIXEL_NUM; i = i + 3)
      {
        ws281x_setPixelColor(i + q, 0); // turn every third pixel off
      }
    }
  }
}

// Theatre-style crawling lights with rainbow effect
void ws281x_theaterChaseRainbow(uint8_t wait)
{
  for (int j = 0; j < 256; j++)
  { // cycle all 256 colors in the wheel
    for (int q = 0; q < 3; q++)
    {
      for (uint16_t i = 0; i < PIXEL_NUM; i = i + 3)
      {
        ws281x_setPixelColor(i + q, ws281x_wheel((i + j) % 255)); // turn every third pixel on
      }
      ws281x_show();

      Delay_Ms(wait);

      for (uint16_t i = 0; i < PIXEL_NUM; i = i + 3)
      {
        ws281x_setPixelColor(i + q, 0); // turn every third pixel off
      }
    }
  }
}

uint32_t ColorHSV(uint16_t hue, uint8_t sat, uint8_t val)
{

  uint8_t r, g, b;
  uint8_t red, green, blue;

  // Remap 0-65535 to 0-1529. Pure red is CENTERED on the 64K rollover;
  // 0 is not the start of pure red, but the midpoint...a few values above
  // zero and a few below 65536 all yield pure red (similarly, 32768 is the
  // midpoint, not start, of pure cyan). The 8-bit RGB hexcone (256 values
  // each for red, green, blue) really only allows for 1530 distinct hues
  // (not 1536, more on that below), but the full unsigned 16-bit type was
  // chosen for hue so that one's code can easily handle a contiguous color
  // wheel by allowing hue to roll over in either direction.
  hue = (hue * 1530L + 32768) / 65536;
  // Because red is centered on the rollover point (the +32768 above,
  // essentially a fixed-point +0.5), the above actually yields 0 to 1530,
  // where 0 and 1530 would yield the same thing. Rather than apply a
  // costly modulo operator, 1530 is handled as a special case below.

  // So you'd think that the color "hexcone" (the thing that ramps from
  // pure red, to pure yellow, to pure green and so forth back to red,
  // yielding six slices), and with each color component having 256
  // possible values (0-255), might have 1536 possible items (6*256),
  // but in reality there's 1530. This is because the last element in
  // each 256-element slice is equal to the first element of the next
  // slice, and keeping those in there this would create small
  // discontinuities in the color wheel. So the last element of each
  // slice is dropped...we regard only elements 0-254, with item 255
  // being picked up as element 0 of the next slice. Like this:
  // Red to not-quite-pure-yellow is:        255,   0, 0 to 255, 254,   0
  // Pure yellow to not-quite-pure-green is: 255, 255, 0 to   1, 255,   0
  // Pure green to not-quite-pure-cyan is:     0, 255, 0 to   0, 255, 254
  // and so forth. Hence, 1530 distinct hues (0 to 1529), and hence why
  // the constants below are not the multiples of 256 you might expect.

  // Convert hue to R,G,B (nested ifs faster than divide+mod+switch):
  if (hue < 510)
  { // Red to Green-1
    b = 0;
    if (hue < 255)
    { //   Red to Yellow-1
      r = 255;
      g = hue; //     g = 0 to 254
    }
    else
    {                //   Yellow to Green-1
      r = 510 - hue; //     r = 255 to 1
      g = 255;
    }
  }
  else if (hue < 1020)
  { // Green to Blue-1
    r = 0;
    if (hue < 765)
    { //   Green to Cyan-1
      g = 255;
      b = hue - 510; //     b = 0 to 254
    }
    else
    {                 //   Cyan to Blue-1
      g = 1020 - hue; //     g = 255 to 1
      b = 255;
    }
  }
  else if (hue < 1530)
  { // Blue to Red-1
    g = 0;
    if (hue < 1275)
    {                 //   Blue to Magenta-1
      r = hue - 1020; //     r = 0 to 254
      b = 255;
    }
    else
    { //   Magenta to Red-1
      r = 255;
      b = 1530 - hue; //     b = 255 to 1
    }
  }
  else
  { // Last 0.5 Red (quicker than % operator)
    r = 255;
    g = b = 0;
  }

  // Apply saturation and value to R,G,B, pack into 32-bit result:
  uint32_t v1 = 1 + val;  // 1 to 256; allows >>8 instead of /255
  uint16_t s1 = 1 + sat;  // 1 to 256; same reason
  uint8_t s2 = 255 - sat; // 255 to 0

  //转换位RGB
  red = ((((((r * s1) >> 8) + s2) * v1) & 0xff00) << 8);
  green = (((((g * s1) >> 8) + s2) * v1) & 0xff00);
  blue = (((((b * s1) >> 8) + s2) * v1) >> 8);

  return red | green | blue;
}
